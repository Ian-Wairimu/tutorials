public class ControlStatement {
    public static void main(String[] args) {
        //if statement
        int num = 70;
        if(num == 70){
            System.out.println("greater");
        }
        //nested if statement
        int num1 = 70;
        if (num1 < 100){
            System.out.println("less than 100");
        }
        if (num1 > 50){
            System.out.println("greater than 100");
        }

    }
}
