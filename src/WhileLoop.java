public class WhileLoop {
    public static void main(String[] args) {
        int i = 0;
        while (i < 10){
            System.out.println(i);
            i++;
        }
        int j = 10;
        while (j>1){
            System.out.println(j);
            j++;
        }
        int arr[] = {2, 3, 5, 76, 90};
        int a = 0;
        while (a < arr.length){
            System.out.println(arr[a]);
            i++;
        }
    }
}
